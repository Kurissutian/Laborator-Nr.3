public class Brand1 {
    private String name;
    private Integer foundationYear;

    public Brand1(String name, Integer foundationYear) {
        this.name = name;
        this.foundationYear = foundationYear;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getFondationYear() {
        return foundationYear;
    }

    public void setFondationYear(Integer fondationYear) {
        this.foundationYear = fondationYear;
    }
}